public class ArrayDuplo {
    public static void main(String[] args) {
        String[] alunos={"Ana","Bruno","Carlos","Diana"};
        double[] notas={8.5,7.3,9.0,6.0};

        for(int i =0; i< alunos.length; i++){
            System.out.println("Alunos: "+alunos[i]+" -notas: "+notas[i]);
        }
    }
    
}
