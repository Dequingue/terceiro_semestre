public class For {
    public static void main(String[] args) {
        System.out.println("\nNúmeros Impares: ");
        for(int i =1 ; i<=52; i++){
            if(i%2!=0){
                System.out.println("contagem "+i);
            }
        }
        System.out.println("\nNúmeros Pares: ");
        for(int i = 1; i<=11;i++){
            if(i%2==0){
                System.out.println("contagem "+i);
            }
        }
    }
}
