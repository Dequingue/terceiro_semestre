public class Animal {
    private String nome;
    private int patas;
    private String cor;
    public Animal(String nome, int patas, String cor) {
        this.nome = nome;
        this.patas = patas;
        this.cor = cor;
    }
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getPatas() {
        return patas;
    }
    public void setPatas(int patas) {
        this.patas = patas;
    }
    public String getCor() {
        return cor;
    }
    public void setCor(String cor) {
        this.cor = cor;
    }

    
    
}
