class Pessoa {
    private String nome;
    private int numeroAviao;
    
    public Pessoa(String nome, int numeroAviao) {
        this.nome = nome;
        this.numeroAviao = numeroAviao;
    }
    
    public String getNome() {
        return nome;
    }
    
    public int getNumeroAviao() {
        return numeroAviao;
    }
}
